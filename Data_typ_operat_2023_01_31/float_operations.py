# Floats
#  Z = {..., -2.5 , ..., -2.0, ... , -1.0, ... , 0, ... , 1.0, ... , 2.0, ... , 2.5, ...}
# Note that there can be endless number possibilities between two floats.


# Float Operations
x = 2.5
y = 4.3

c = x + y
print(c)

c = x-y
print(round(c,2))

c= x * y
print(c)

c= x/y
print(c)

c= x//y
print(c)

c = x%y
print(c)

c= x**y
print(c)


